#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*str;

	if (count > 0 && size > ULLONG_MAX / count)
		return (NULL);
	str = malloc(count * size);
	if (!str)
		return (NULL);
	ft_bzero(str, count * size);
	return (str);
}
