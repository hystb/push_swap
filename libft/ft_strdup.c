#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;
	int		size;

	size = ft_strlen(src);
	i = 0;
	dest = (char *)malloc(sizeof(char) * size + 1);
	if (!(dest))
		return (dest);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
